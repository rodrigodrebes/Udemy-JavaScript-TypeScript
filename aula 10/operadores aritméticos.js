/* 
* Aritméticos (+ - / *)
* + = adição e concatenação
* (**) = potenciação
* % = resto da divisão
* ++ = operador de incremento
* -- = operador de decremento
*/
const num1 = 5
const num2 = parseInt('10') // converte para num inteiro ou float, em parseFloat ou Number
console.log(num1+num2); // adiciona e concatena

let contador = 1; // sempre LET.
contador++;
// ou
++contador
console.log(contador)

let contador1 = 1;
contador1 --;
console.log(contador1)
